    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         btnBuscar.setToolTipText("Clique para ver os dados do paciente");

        String cpf = textCPF.getText().trim();

        if (cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite o CPF.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            Usuario usuario = usuarioService.buscarPorCpf(cpf);

            if (usuario == null) {
                JOptionPane.showMessageDialog(this,
                        "Nenhum usuário encontrado para o CPF: " + cpf,
                        "Resultado da Busca", JOptionPane.INFORMATION_MESSAGE);
            } else {
                Date horaBusca = new Date();
                atualizarTabela(List.of(usuario), horaBusca); // Converte o usuário em uma lista
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this,
                    "Ocorreu um erro ao buscar os usuários. Tente novamente.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAtenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtenderActionPerformed
        int linhaSelecionada = tabelaFila.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um paciente na fila para atender.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tabelaFila.getModel();
        model.removeRow(linhaSelecionada);
        JOptionPane.showMessageDialog(this, "Paciente atendido com sucesso!");

        TelaProntuario telaProntuario = new TelaProntuario();
        telaProntuario.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnAtenderActionPerformed

