    /**
     * Método para atualizar a tabela com o nome do paciente.
     *
     * @param usuarios lista de usuários
     * @param horaBusca horário em que o usuário foi buscado
     */
    private void atualizarTabela(List<Usuario> usuarios, Date horaBusca) {
        DefaultTableModel model = (DefaultTableModel) tabelaFila.getModel();
        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");

        for (Usuario usuario : usuarios) {
            model.addRow(new Object[]{
                usuario.getNome(),
                sdf.format(horaBusca)
            });
        }
    }

