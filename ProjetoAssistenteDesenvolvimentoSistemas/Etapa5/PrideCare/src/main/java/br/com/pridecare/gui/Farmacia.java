    /**
     * Método para atualizar a tabela com as receitas encontradas.
     *
     * @param receitas a lista de receitas a serem exibidas na tabela
     */
    private void atualizarTabela(List<Receita> receitas) {
        DefaultTableModel model = (DefaultTableModel) jTableReceitas.getModel();
        model.setRowCount(0);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (Receita receita : receitas) {
            model.addRow(new Object[]{
                receita.getId(),
                sdf.format(receita.getDataCriacao()),
                receita.getStatus()
            });
        }
    }
    
