    private void textCPFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textCPFActionPerformed
        String cpf = textCPF.getText();

        if (cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um CPF para realizar a busca.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        List<Receita> receitas = receitaService.buscarPorCpf(cpf);

        if (receitas.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhuma receita encontrada para o CPF: " + cpf,
                    "Resultado da Busca", JOptionPane.INFORMATION_MESSAGE);
        } else {
            atualizarTabela(receitas);
        }
    }//GEN-LAST:event_textCPFActionPerformed
    /**
     * Ação realizada ao pressionar o botão de Buscar. Este método deve realizar
     * a busca por pacientes com base no nome inserido.
     *
     * @param evt o evento acionado ao clicar no botão de Buscar
     */
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        btnBuscar.setToolTipText("Clique para ver a listagem de receitas do paciente");

        String cpf = textCPF.getText();

        if (cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um CPF para buscar.",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        List<Receita> receitas = receitaService.buscarPorCpf(cpf);

        if (receitas.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhuma receita encontrada para o CPF: " + cpf,
                    "Resultado da Busca", JOptionPane.INFORMATION_MESSAGE);
        } else {
            atualizarTabela(receitas);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

