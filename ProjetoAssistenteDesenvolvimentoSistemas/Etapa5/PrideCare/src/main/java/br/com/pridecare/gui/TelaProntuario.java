    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String medicamento1 = textMedicamento1.getText();
        String quantidade1 = textQuantidade.getText();
        String medicamento2 = textMedicamento2.getText();
        String quantidade2 = textQuantidade1.getText();
        String medicamento3 = textMedicamento3.getText();
        String quantidade3 = textQuantidade2.getText();

        String anotacao = textAnotacao.getText();

        // Lista de medicamentos e quantidades
        List<String> medicamentos = Arrays.asList(medicamento1, medicamento2, medicamento3);
        List<String> quantidadesStr = Arrays.asList(quantidade1, quantidade2, quantidade3);

        // Cria a receita e salva no banco de dados
        Receita receita = new Receita();
        receita.setCpfUsuario(pacienteSelecionado.getCpf());
        receita.setDataCriacao(new java.sql.Date(System.currentTimeMillis()));
        receita.setStatus(StatusReceita.DISPONIVEL);

        receitaService.salvarReceita(receita);

        // Salva os medicamentos e quantidades associados à receita, se houver
        for (int i = 0; i < medicamentos.size(); i++) {
            String medicamento = medicamentos.get(i);
            String quantidadeStr = quantidadesStr.get(i);

            if (!medicamento.isEmpty() && !quantidadeStr.isEmpty()) {
                int quantidade;
                try {
                    quantidade = Integer.parseInt(quantidadeStr);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Quantidade inválida para o medicamento: " + medicamento,
                            "Erro", JOptionPane.ERROR_MESSAGE);
                    continue;
                }

                ReceitaMedicamento receitaMedicamento = new ReceitaMedicamento();
                receitaMedicamento.setReceita(receita);
                receitaMedicamento.setMedicamento(medicamento);
                receitaMedicamento.setQuantidade(quantidade);

                receitaMedicamentoService.salvarReceitaMedicamento(receitaMedicamento);
            }
        }
        JOptionPane.showMessageDialog(this, "Receita salva com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSalvarActionPerformed

