    /**
     * Método para atualizar a tabela com o nome do paciente.
     *
     * @param pacientes lista de pacientes
     */
    private void atualizarTabela(List<Usuario> pacientes) {
        DefaultTableModel model = (DefaultTableModel) tabelaPacienteConsulta.getModel();
        model.setRowCount(0); // Limpar a tabela antes de adicionar novas linhas

        for (Usuario paciente : pacientes) {
            model.addRow(new Object[]{
                paciente.getNome()
            });
        }
    }

